#include<iostream>
#include <cmath>

using namespace std;

int main() {

    // power
    cout << pow(2, 5) << endl;

    // squareroot
    cout << sqrt(36) << endl;

    // rounding
    cout << round(4.6) << endl;

    // round up
    cout << ceil(4.1) << endl;

    // round down 
    cout << floor(4.8) << endl;

    // compare and return max
    cout << fmax(3, 10) << endl;
    cout << fmin(3, 10) << endl;

}